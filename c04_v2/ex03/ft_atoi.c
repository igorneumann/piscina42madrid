#include <stdio.h>

int		ft_atoi(char *str)
{
	int		i;
	int		s;
	int		x;

	s = 1;
	i = 0;
	x = 0;
	while ((str[i] <= 13 && str[i] >= 9) || (str[i] == 32))
		i++;
	while (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			s = s * (-1);
		i++;
	}
	while (str[i] <= 57 && str[i] >= 48)
	{
		x = x * 10 + (str[i] - 48);
		i++;
	}
	x = x * s;
	return (x);
}
