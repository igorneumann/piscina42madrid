char	*ft_strcat(char *dest, char *src)
{
	unsigned int c;

	c = 0;
	while (dest[c])
		c++;
	while (*src != '\0')
	{
		dest[c] = *src;
		src++;
		c++;
	}
	dest[c] = '\0';
	return (dest);
}
